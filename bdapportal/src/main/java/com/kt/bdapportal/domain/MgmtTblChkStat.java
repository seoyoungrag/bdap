package com.kt.bdapportal.domain;

import java.sql.Timestamp;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "MGMT_TBL_CHK_STAT")
public class MgmtTblChkStat {

	
	@Column(name="CHK_DT", nullable=false)
	private Timestamp chkDt;
	
	@Column(name="DB_NM", nullable=false)
	private String dbNm;
	
	@Column(name="TBL_NM", nullable=false)
	private String tblNm;
	
	@Column(name="TBL_CHK_TYPE", nullable=false)
	private String tblChkType;
	
	@Column(name="COL_NM", nullable=false)
	private String colNm;
	
	@Column(name="QRY_PARENT_ID", nullable=false)
	private String qryParentId;
	
	@Id
	@Column(name="QRY_ID", nullable=false)
	private String qryId;
	
	@Column(name="QRY_RESULT", nullable=false)
	private String qryResult;
	
	@Column(name="QRY_DESC", nullable=true)
	private String qryDesc;
	
	@Column(name="QRY_CHK_FORMAT", nullable=true)
	private String qryChkFormat;

	public Timestamp getChkDt() {
		return chkDt;
	}

	public void setChkDt(Timestamp chkDt) {
		this.chkDt = chkDt;
	}

	public String getDbNm() {
		return dbNm;
	}

	public void setDbNm(String dbNm) {
		this.dbNm = dbNm;
	}

	public String getTblNm() {
		return tblNm;
	}

	public void setTblNm(String tblNm) {
		this.tblNm = tblNm;
	}

	public String getTblChkType() {
		return tblChkType;
	}

	public void setTblChkType(String tblChkType) {
		this.tblChkType = tblChkType;
	}

	public String getColNm() {
		return colNm;
	}

	public void setColNm(String colNm) {
		this.colNm = colNm;
	}

	public String getQryParentId() {
		return qryParentId;
	}

	public void setQryParentId(String qryParentId) {
		this.qryParentId = qryParentId;
	}

	public String getQryId() {
		return qryId;
	}

	public void setQryId(String qryId) {
		this.qryId = qryId;
	}

	public String getQryResult() {
		return qryResult;
	}

	public void setQryResult(String qryResult) {
		this.qryResult = qryResult;
	}

	public String getQryDesc() {
		return qryDesc;
	}

	public void setQryDesc(String qryDesc) {
		this.qryDesc = qryDesc;
	}

	public String getQryChkFormat() {
		return qryChkFormat;
	}

	public void setQryChkFormat(String qryChkFormat) {
		this.qryChkFormat = qryChkFormat;
	}
	

	
	
}
