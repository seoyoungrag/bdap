package com.kt.bdapportal.common.util.auth;

import java.util.List;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown=true)
public class NdapRole {
	int id;
	String name;
	String comment;
	List<NdapPriv> ndapPrivileges;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public List<NdapPriv> getNdapPrivileges() {
		return ndapPrivileges;
	}
	public void setNdapPrivileges(List<NdapPriv> ndapPrivileges) {
		this.ndapPrivileges = ndapPrivileges;
	}

}
